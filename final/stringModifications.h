/*
Gets a char pointer to line.
Retrns 1 if line composed of white-spaces only, and 0 otherwise.
*/
int isempty(char *line);


/*
Gets a char pointer to a string.
Removes white-spaces from beginning and ending of a string.
*/
void removeWhitespaces(char *string);


/*
Gets a char pointer to a word.
Removes it's first letter.
*/
void removeFirstLetter(char *word);
